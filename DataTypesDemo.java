public class DataTypesDemo {

    public static void main(String[] args) {

        // Variable declarations
        int age = 20;
        float salary = 25000.50f;
        char grade = 'A';
        boolean isPassed = true;

        // Display values
        System.out.println("Integer Value (Age): " + age);
        System.out.println("Float Value (Salary): " + salary);
        System.out.println("Character Value (Grade): " + grade);
        System.out.println("Boolean Value (Passed): " + isPassed);
    }
}